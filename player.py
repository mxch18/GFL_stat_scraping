class Player():
    def __init__(self, first_name, last_name, nationality="", position=[], yob=-1, height=-1, weight=-1):
        self.first_name = first_name
        self.last_name = last_name
        self.nationality = nationality
        self.position = position
        self.yob = yob  # year of birth
        self.height = height
        self.weight = weight
